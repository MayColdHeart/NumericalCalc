function sum(num_1:number, num_2:number){
    return num_1 + num_2
};

function subt(num_1:number, num_2:number){
    return num_1 - num_2
};

function multp(num_1:number, num_2:number){
    return num_1 * num_2
};

function div(num_1:number, num_2:number){
    return num_1 / num_2
};


console.log(sum(4, 3));
console.log(subt(5, 7));
console.log(multp(5, 5));
console.log(div(4.2, 2));
